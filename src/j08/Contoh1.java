package j08;


public class Contoh1 {
    public static void main(String[] args){
        //PERULANGAN FOR
   int x;
   for(x = 1; x<=5; x++)
   {
       System.out.println("SMK Telkom Malang");
   }    
    
    //PENGULANGAN WHILE
   int i = 1;
   while(i <= 5)
   {
    System.out.println("SMK Telkom Malang");
    i++;
   }
   
   //PENGULANGAN DO WHILE
   int j = 1;
   do
   {
       System.out.println("SMK Telkom Malang");
       j++;
   }
   while(j <= 5);
    
    
    
    
    
    }
   
}
